// nodejs中有多种类型的值,包括字符串、数字、布尔、null、undefined,
// 下面是一些基础的例子。

// 字符串可以通过 + 连接。
console.log("a" + "b");

// 数字
console.log(1);

// null 和 undefined
console.log(null);
console.log(undefined);

// 布尔型，以及常见的布尔操作。
console.log(false || true);
console.log(false && false);
console.log(!false);

// 变量的声明可以使用 var 关键字
var name = "cat";
var age = 3;

// 在最新的js语法中也可以使用 let 关键字来声明
let name = "cat";
let age = 3;